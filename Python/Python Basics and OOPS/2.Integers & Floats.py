# Addition
print(3 + 2)

# Subtraction
print(3 - 2)

# Multiplication
print(3 * 2)

# Division
print(3 / 2)

# Floor Division
print(3 // 2)

# Expomene
print(3 ** 2)

# Modulus
print(3 % 2)

# Absolute number
print(abs(-3))

# Round number
print(round(2.65))
print(round(3.15))
print(round(3.75, 1))

# Boolean Numbers

# Equal
a = 1
b = 2
print(a == b)

# Not Equal
print(a != b)

# Greater
print(a > b)

# Lesser
print(a < b)

# Greater than or equal to
print(a >= b)

# Less than or equal to
print(a <= b)
